# Operators - + - * / // % ** and comparison operators
a = 10
b = 3
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)   # float division
print("a // b =", a // b) # integer division
print("a % b =", a % b)   # remainder
print("a ** b =", a ** b) # power
print("a > b ?", a > b)
print("a == b ?", a == b)
